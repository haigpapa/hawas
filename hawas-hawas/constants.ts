// Helper function to create the XP curve programmatically
const createXpCurve = (levels: number): number[] => {
    const xpTable: number[] = [0]; // Level 1 requires 0 XP
    let currentXp = 0;
    const baseIncrement = 500;
    const factor = 1.05;

    for (let i = 2; i <= levels; i++) {
        const increment = Math.floor(baseIncrement * Math.pow(factor, i - 2));
        currentXp += increment;
        xpTable.push(currentXp);
    }
    return xpTable;
};

